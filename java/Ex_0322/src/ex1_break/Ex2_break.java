package ex1_break;

public class Ex2_break {
	public static void main(String[] args) {
		
		int n = 1;
		
		while( true ) {
			
			System.out.println(n);
			n++;
			
			if( n > 5 ) {
				break;
			}
			
		}//while
		
		System.out.println("end");
		
	}//main
}









